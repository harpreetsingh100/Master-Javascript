// The increment and decrement operators in JavaScript will add one (+1) or subtract one (-1), respectively, to their operand, and then return a value.

const x = 1;

// Increment — x++ OR ++x
// Decrement — x-- OR --x

// As you can see, the ++/ —- operators can be used before or after the operand. Here’s what that might look in your code:

// Increment

// When you use the increment/decrement operator after the operand, the value will incerement/decrement later.

x++;

// Decrement

// When you use the increment/decrement operator beofre the operand, the value will be returned increment/decrement immediately.

--x;
